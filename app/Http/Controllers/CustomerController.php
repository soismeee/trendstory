    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        // dd($request);
        $rules = $request->validate([
            'aktif' => 'required'
        ]);
        $rules['name'] = $request->name;
        $rules['username'] = $request->username;
        $rules['alamat'] = $request->alamat;
        User::where('id', $id)->update($rules);
        return redirect('/dashboard/customers')->with('edit', 'Data customers berhasil diubah!!!');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        User::destroy($id);
        return redirect('/dashboard/customers')->with('hapus', 'Data customer berhasil dihapus!!!');
    }
